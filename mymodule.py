def add(a,b):
    ''' This function is used to add the elements'''
    c=a+b
    print(c,"add")
def sub(a,b):
    ''' This function is used to substract the elements'''
    c=a-b
    print(c,"Sub")
def mul(a,b):
    ''' This function is used to multiply the elements'''
    c=a*b
    print(c,"mul(a,b)")
    
def remainder(a,b):
    ''' this will give the remainder of the elements'''
    c=a/b
    print(c,"remainder")
def quot(a,b):
    ''' this will quote the elements'''
    c=a%b
    print(c,"quote")
